a = 126 
b = 130 

print (a + b)  # Output: 256

# print (a * b)  # Output: 16380 

print (a - b)  # Output: -4

print (a / b)  # Output: 0.9692307692307692

print (a // b)  # Output: 0

print (a % b)  # Output: 126

# print (a ** b)  # Output: 126 raised to the power of 130, which is a very large number

print (a > b)  # Output: False

print (a < b)  # Output: True

print (a == b)  # Output: False

print (a != b)  # Output: True

print (a >= b)  # Output: False

print (a <= b)  # Output: True  

print (a and b)  # Output: 130 (since both a and b are non-zero, it returns the last evaluated value)

print (a or b)  # Output: 126 (since a is non-zero, it returns the first evaluated value)

print (not a)  # Output: False (since a is non-zero, it returns False)

print (not b)  # Output: False (since b is non-zero, it returns False)

print (a is b)  # Output: False (since a and b are different objects in memory)

print (a is not b)  # Output: True (since a and b are different objects in memory)